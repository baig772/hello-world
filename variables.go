package main

import "fmt"

func main() {
	var name = "John Doe"
	fmt.Println("Name is => " + name)

	var digit1, digit2 = 1, 2
	fmt.Println(digit1)
	fmt.Println(digit2)

	var isTrue = true
	fmt.Println(isTrue)

	var isFalse = false
	fmt.Println(isFalse)

	secondName := "Jane Doe"
	fmt.Println("Second Name is =>" + secondName)
}
